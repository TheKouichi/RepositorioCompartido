package clases;

import java.util.Scanner;

public class Circulo 
{
	private double radio;
	private double radioDet;
	
	public Circulo(double radio, double radioDet)
	{
		this.radio = radio;
		this.radioDet = radioDet;
	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public Circulo()
	{
		Scanner entrada = new Scanner (System.in);
		System.out.println("Introduzca el radio del Circulo 1: ");
		radio = entrada.nextDouble();
		radioDet = 2;
	}
	////////////////////////////////////////////////////////////////////////////////////////////////////////
	public Circulo(Circulo copia)
	{
		radio = copia.radio;
		radioDet = copia.radioDet;
	}
	/////////////////////////////////////////////////////////////////////////////////////////////////////////
	public double getRadio()
	{
		return radio;
	}
	////////////////////////////////////////////////////////////////////////////////////////////////////////
	public double getRadioDet() {
		return radioDet;
	}
	////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setRadio(double radio)
	{
		this.radio = radio;
	}
	////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void setRadioDet(double radioDet)
	{
		this.radioDet = radioDet;
	}
	////////////////////////////////////////////////////////////////////////////////////////////////////////
	public double calcularSuperficie()
	{
		double pi = 3.141592;
		return pi * (radio * radio);
	}
	////////////////////////////////////////////////////////////////////////////////////////////////////////
	public double calcularSuperficieDet()
	{
		double pi = 3.141592;
		return pi * (radioDet * radioDet);
	}
}
	