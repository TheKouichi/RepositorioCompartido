package clases;

public class ClaseCadena {

	public static void main(String[] args) {
		String frase = "Hola Mundo";
		System.out.println(frase);
		int longitud = frase.length();
		System.out.println("La longitud de esta frase es de: " + longitud);
		char car = frase.charAt(3);
		System.out.println("El cuarto caracter de esta frase es: " + car);

	}

}
